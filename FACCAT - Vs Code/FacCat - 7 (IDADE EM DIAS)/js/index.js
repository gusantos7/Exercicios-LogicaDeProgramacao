let ano = parseInt(prompt("Digite a quantidade de anos vividos: "))
let mes = parseInt(prompt("Digite a quantidade de meses passado do último aniversário: "))
let dia = parseInt(prompt("Digite a quantidade de dias passados do seu último mesversário: "))

let diasvividos = ano*365+mes*30+dia

alert("Você ja viveu " + diasvividos + " dias, Parabéns!!")